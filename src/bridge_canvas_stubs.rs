//! Frame.canvas bridge function stubs for the server runtime.
//!
//! The frame.canvas plugin is a client-only Layer 4 plugin: canvas drawing,
//! audio, and input are handled in the browser. However, WASM modules that use
//! `canvasScene:` blocks emit imports for every `_canvas_*`, `_input_*`,
//! `_audio_*`, `_sprite_*`, etc. bridge function regardless of whether the
//! module ultimately runs server-side.
//!
//! Without these stubs, `Linker::instantiate` fails with `unknown import: env::_canvas_init`
//! whenever the server loads a `.wasm` that has any canvas scene in it.
//!
//! This file registers a no-op for every bridge entry declared in
//! `clean-framework/plugins/frame.canvas/plugin.toml [bridge]`. The signatures
//! match the plugin.toml declarations exactly so the linker is satisfied.
//! None of these stubs perform any work — canvas scenes are not executed
//! server-side.
//!
//! AUTO-GENERATED by `tools/gen_canvas_stubs.py`. Do not edit by hand.
//! To regenerate after updating frame.canvas/plugin.toml:
//!   python3 tools/gen_canvas_stubs.py > src/bridge_canvas_stubs.rs

use crate::error::{RuntimeError, RuntimeResult};
use crate::wasm::WasmState;
